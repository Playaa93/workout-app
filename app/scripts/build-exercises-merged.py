def generate_image_mapping(source_exercises, existing_names):
    """Generate exercise-images.ts with mapping for all exercises."""
    # Build name → id from free-exercise-db source data
    source_map = {}
    for ex in source_exercises:
        source_map[ex['name']] = ex['id']

    # Start with manual overrides (for our 124 enriched exercises)
    mapping = dict(MANUAL_IMAGE_OVERRIDES)

    # Add all free-exercise-db exercises (name → native id)
    for ex in source_exercises:
        name = ex['name']
        if name not in mapping:
            mapping[name] = ex['id']

    ts_path = os.path.join(APP_DIR, 'src', 'lib', 'exercise-images.ts')
    lines = [
        '/**',
        ' * Auto-generated by scripts/build-exercises-merged.py',
        ' * Maps exercise English names to free-exercise-db image directory IDs.',
        ' * Do not edit manually — run: python3 scripts/build-exercises-merged.py',
        ' */',
        '',
        'const IMAGE_MAP: Record<string, string> = {',
    ]
    for name in sorted(mapping.keys()):
        img_id = mapping[name]
        escaped_name = name.replace("'", "\\'")
        escaped_id = img_id.replace("'", "\\'")
        lines.append(f"  '{escaped_name}': '{escaped_id}',")
    lines.append('};')
    lines.append('')
    lines.append("const BASE_URL = 'https://raw.githubusercontent.com/yuhonas/free-exercise-db/main/exercises';")
    lines.append('')
    lines.append('/**')
    lines.append(' * Get image URLs for an exercise (typically 2 images: start and end position).')
    lines.append(' */')
    lines.append('export function getExerciseImages(exerciseNameEn: string | null): string[] {')
    lines.append('  if (!exerciseNameEn) return [];')
    lines.append('  const id = IMAGE_MAP[exerciseNameEn];')
    lines.append('  if (!id) return [];')
    lines.append('  return [`${BASE_URL}/${id}/0.jpg`, `${BASE_URL}/${id}/1.jpg`];')
    lines.append('}')
    lines.append('')
    lines.append('/**')
    lines.append(' * Get the primary (start position) image URL for an exercise.')
    lines.append(' */')
    lines.append('export function getExerciseImage(exerciseNameEn: string | null): string | null {')
    lines.append('  const images = getExerciseImages(exerciseNameEn);')
    lines.append('  return images[0] ?? null;')
    lines.append('}')
    lines.append('')
    lines.append('/**')
    lines.append(' * Check if images are available for an exercise.')
    lines.append(' */')
    lines.append('export function hasExerciseImages(exerciseNameEn: string | null): boolean {')
    lines.append('  return !!exerciseNameEn && exerciseNameEn in IMAGE_MAP;')
    lines.append('}')
    lines.append('')

    with open(ts_path, 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines))

    print(f'Generated {ts_path} with {len(mapping)} image mappings')


