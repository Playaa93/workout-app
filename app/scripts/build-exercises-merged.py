def slugify(name: str) -> str:
    """Convert exercise name to kebab-case ID."""
    s = name.lower().strip()
    s = re.sub(r'[^a-z0-9\s-]', '', s)
    s = re.sub(r'[\s]+', '-', s)
    s = re.sub(r'-+', '-', s)
    return s.strip('-')


def map_primary_muscles(muscles: list[str]) -> list[str]:
    """Map free-exercise-db muscle names to our anatomical names."""
    return [PRIMARY_MUSCLE_MAP.get(m, m) for m in muscles]


def map_equipment(equip=None):
    """Map equipment string to our equipment list."""
    if not equip or equip == 'None':
        return ['bodyweight']
    return [EQUIPMENT_MAP.get(equip, equip)]


def convert_exercise(ex: dict) -> dict:
    """Convert a free-exercise-db exercise to our schema."""
    name_en = ex['name']
    name_fr = TRANSLATIONS.get(name_en, name_en)  # Fallback to English if missing
    primary_muscle = ex['primaryMuscles'][0] if ex['primaryMuscles'] else 'other'
    muscle_group = MUSCLE_GROUP_MAP.get(primary_muscle, 'full_body')

    return {
        'id': slugify(name_en),
        'name_fr': name_fr,
        'name_en': name_en,
        'aliases': [],
        'movement_pattern': None,
        'exercise_type': ex.get('mechanic') or None,  # compound/isolation
        'muscle_group': muscle_group,
        'primary_muscles': map_primary_muscles(ex.get('primaryMuscles', [])),
        'secondary_muscles': map_primary_muscles(ex.get('secondaryMuscles', [])),
        'stabilizers': [],
        'equipment': map_equipment(ex.get('equipment')),
        'equipment_alternatives': [],
        'good_for': None,
        'bad_for': None,
        'modifications': [],
        'difficulty': DIFFICULTY_MAP.get(ex.get('level', ''), 'intermediate'),
        'technique_cues': [],
        'common_mistakes': [],
        'instructions': ex.get('instructions', []),
        'source': 'manual',
        'image_url': None,
        'video_url': None,
    }


def main():
    # Load source data
    with open(os.path.join('/tmp', 'free-exercises.json')) as f:
        new_exercises = json.load(f)

    with open(os.path.join(APP_DIR, 'data', 'exercises-enriched.json')) as f:
        existing_data = json.load(f)
        existing_exercises = existing_data['exercises']

    # Build set of existing names (lowercased) for dedup
    existing_names = set(e['name_en'].lower().strip() for e in existing_exercises)

    # Convert new exercises, dedup by name
    seen = set()
    converted = []
    untranslated = []

    for ex in new_exercises:
        name = ex['name']
        if name in seen or name.lower().strip() in existing_names:
            continue
        seen.add(name)

        entry = convert_exercise(ex)
        converted.append(entry)

        if name not in TRANSLATIONS:
            untranslated.append(name)

    # Merge: existing (enriched) first, then new
    merged = existing_exercises + converted

    # Output
    output = {
        'version': '2.0.0',
        'source': 'SuperPhysique/Delavier + free-exercise-db',
        'exercises': merged,
    }

    out_path = os.path.join(APP_DIR, 'data', 'exercises-merged.json')
    with open(out_path, 'w', encoding='utf-8') as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    print(f'Merged: {len(existing_exercises)} existing + {len(converted)} new = {len(merged)} total')
    if untranslated:
        print(f'\nWARNING: {len(untranslated)} exercises without French translation:')
        for n in untranslated:
            print(f'  - {n}')
    else:
        print('All exercises translated to French!')

    # ── Generate exercise-images.ts ──
    # Build name_en → free-exercise-db ID mapping for ALL exercises
    # 1. For new exercises: use the native `id` field from free-exercise-db
    # 2. For existing 124: use manual overrides (our name_en often differs from free-exercise-db name)
    generate_image_mapping(new_exercises, existing_names)


